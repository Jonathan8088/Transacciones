public class Elemento2 {
    private int id;
    
    private String objeto;

    public Elemento2() {
    }

    public Elemento2(int id, String objeto) {
        this.id = id;
        this.objeto = objeto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getObjeto() {
        return objeto;
    }

    public void setObjeto(String objeto) {
        this.objeto = objeto;
    }
    
    
}
